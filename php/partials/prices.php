<div class="row">
    <p>
     <strong>NOS DIFFERENTES CATEGORIES DE KARTS : </strong>
        <br>
        <strong>Mini 160cc: </strong>
        <br>
         Le « mini kart » 160cc est une réplique exacte du karting adulte à une échelle inférieure. Cela permet
         aux enfants dès l’âge de 7 ans jusqu'à 13/14 ans de faire comme les grands (une taille minimum d'1m30 pour
         le conduire est obligatoire).
        <br><br>
        <strong>Elite : </strong>
        <br>
        Le karting dit « adulte » 270cc accessible dès l’âge de 13/14 ans, est un karting de toute dernière génération,
        qui va vous laisser rêver et vous permettre de vous comparer aux grands noms du sport automobile !
        <br><br>
        <strong>Bi-place : </strong>
        <br>
        permet à un adulte de piloter le karting avec à ses côtés, un enfant (minimum 5 ans ET 1m10) ou une autre personne.
        <br><br>
        <strong>Handikart :</strong>
        <br>
        avec ses commandes au volant, il permet le pilotage du karting pour certains types de handicaps.
    </p>
</div>

<div class="row">
